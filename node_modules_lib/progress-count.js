module.exports = function progressCount(each_elem, all_elems) {
	var PROGRESS = (each_elem / all_elems * 100).toFixed(2);
	PROGRESS < 10.00 ? PROGRESS = '0' + PROGRESS : PROGRESS;
	if (PROGRESS < 100) {
		return PROGRESS;
	} else if (PROGRESS == 100) {
		return '99.99';
	} else {
		return ' 100 ';
	}
}