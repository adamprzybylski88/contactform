const applyDefaults = require('./apply-defaults.js');
const timeNow = require('./time-now.js');

module.exports = function(objects, message, options) {
	let progress;

	let defaults = {
		color: 'yellow',
		status: 'start'
	}
	const _options = applyDefaults(options, defaults)

	if (_options.color === 'yellow') {
		message = message.yellow
	} else {
		message = message.green
	}

	if (_options.status === 'start') {
		progress = '['.cyan + '--.--%'.dim + ']'.cyan
	} else {
		progress = '['.cyan + ' 100% '.dim + ']'.cyan
	}
	console.log(
		'[' + timeNow().dim + ']' +
		progress + ' ' + objects + ' '.fileColor + message
	)
}