module.exports = function checkDuplicates(file, arr, file_l) {
	var f_first = true;
	for (var d = 0; d < file_l; d++) {
		if (file == arr[d].file) {
			if (f_first) {
				f_first = false;
			} else {
				console.log(file.cyan + ' file name is duplicated'.red);
				return 'duplicated';
			}
		}
	}
}
