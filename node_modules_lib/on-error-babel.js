module.exports = function(err) {
	let fileName = err.filename || err.fileName
	if (!fileName) {
		console.log(err)
	} else {
		console.log('! - - - ERROR FOUND - - - !'.red)
		if (err.loc) console.log('LINE: '.cyan + (err.loc.line).toString().yellow)
		if (err.loc) console.log('COLUMN: '.cyan + (err.loc.column).toString().yellow)
		console.log('FILE: '.cyan + fileName.substring(fileName.indexOf('src/')).yellow)
		console.log('MESSAGE: '.cyan + (err.name).yellow)
		console.log('! - - - - - - - - - - - - !'.red)
	}
}