const colors = require('colors')

colors.setTheme({
	sizeColor: ['strikethrough', 'bold', 'dim'],
	compressedSizeColor: ['bold'],
	arrowColor: ['cyan'],
	fileColor: ['italic'],
	duplicatedColor: ['bold', 'red']
});

const timeNow = require('./time-now')
const fileSize = require('./file-size')
const timeFromMs = require('./time-from-ms')

module.exports = (instanceName, ext, fileName, dist, time_after) => {
	console.log(
		'[' + timeNow().dim + ']' +
		' Finished ' + ('[' + ext + ']').cyan + ' ' +
		instanceName.underline + ' ' + fileName.fileColor + ' => '.arrowColor +
		fileSize(dist, { interpreter: true }).compressedSizeColor + 
		' after ' + timeFromMs(time_after, { naturalInterpreter: true }).cyan
	)
}