const fs = require('fs')
module.exports = function(filepath, filepathCompressed) {
	if (typeof(filepath) == 'string') filepath = fs.statSync(filepath).size;
	if (typeof(filepathCompressed) == 'string') filepathCompressed = fs.statSync(filepathCompressed).size;
	return ((filepathCompressed / filepath * 100) - 100).toFixed(2) * -1 + '%';
}
