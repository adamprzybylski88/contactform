module.exports = function(options, defaults) {
	if (!options) {
		return defaults
	} else {
		var i = 0;
		for (var k in defaults) {
			if (options && !options.hasOwnProperty(k)) {
				options[k] = defaults[k];
			}
		}
		return options;
	}
}

